package com.ontosim.model;

public class OntoFileMsg {
	String msg_val;
	String msg_cause;

	public String getMsg_val() {
		return msg_val;
	}

	public void setMsg_val(String msg_val) {
		this.msg_val = msg_val;
	}

	public String getMsg_cause() {
		return msg_cause;
	}

	public void setMsg_cause(String msg_cause) {
		this.msg_cause = msg_cause;
	}
}